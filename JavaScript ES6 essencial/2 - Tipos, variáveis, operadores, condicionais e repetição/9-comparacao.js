// Igual (==) - Retorna verdadeiro caso os operadores sejam iguais. 3 == var1
console.log(`\"3\" == 3 : ${"3" == 3}`);
console.log(`\"3\" == \"3\" : ${"3" == "3"}`);
console.log(`\"3\" == 4 : ${"3" == 4}`);
console.log();

// Diferente (!=) - Retorna verdadeiro caso os operadores sejam diferentes. var1 !== 4
console.log(`\"3\" != 4 : ${"3" != 4}`);
console.log();

// Estritamente igual (===) - Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo.
console.log(`3 === 3 : ${3 === 3}`)
console.log(`\"3\" === 3 : ${"3" === 3}`)
console.log();

// Estritamente diferente (!==) - Retorna verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo.
console.log(`3 !== 3 : ${3 !== 3}`)
console.log(`\"3\" !== 3 : ${"3" !== 3}`)
